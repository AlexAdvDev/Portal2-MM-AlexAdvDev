def print_ascii(moj):
    liney1 = ""
    liney2 = ""
    liney3 = ""
    liney4 = ""
    liney5 = ""
    liney6 = ""

    # for each letter in the input string
    for letta in moj:

        # make it uppercase
        letta = letta.upper()

        lettaoutput = "Ascii" + letta

        # Get the first line in letteroutput
        try:
            liney1 = liney1 + eval(lettaoutput)[0]
            liney2 = liney2 + eval(lettaoutput)[1]
            liney3 = liney3 + eval(lettaoutput)[2]
            liney4 = liney4 + eval(lettaoutput)[3]
            liney5 = liney5 + eval(lettaoutput)[4]
            liney6 = liney6 + eval(lettaoutput)[5]
        except:
            liney1 = liney1 + " "
            liney2 = liney2 + " "
            liney3 = liney3 + " "
            liney4 = liney4 + " "
            liney5 = liney5 + " "
            liney6 = liney6 + " "

        # convert liney to a string
        liney1 = str(liney1)
        liney2 = str(liney2)
        liney3 = str(liney3)
        liney4 = str(liney4)
        liney5 = str(liney5)
        liney6 = str(liney6)

    return "//" + liney1 + "\n" + "//" +  liney2 + "\n" + "//" +  liney3 + "\n" + "//" +  liney4 + "\n" + "//" +  liney5 + "\n" + "//" +  liney6 + "\n"





